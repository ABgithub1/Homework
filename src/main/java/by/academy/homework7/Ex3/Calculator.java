package by.academy.homework7.Ex3;

public class Calculator {
    public Calculator() {
    }

    public static int sum(int a, int b) {
        int res = a + b;
        System.out.println(res);
        return res;
    }

    public static int minus(int a, int b) {
        int res = a - b;
        System.out.println(res);
        return res;
    }

    public static int division(int a, int b) {
        int res = a / b;
        System.out.println(res);
        return res;
    }

}
